#include "libft.h"

int	main(void)
{
	int	i;

	i = ft_tolower('C');
	printf("%c", i);
	return (0);
}

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
	{
		c = c + 32;
	}
	return (c);
}
